package modeli;

/**
 *
 * @author Programer10
 */
public class Kategorija {
    int id;
    String ime;

    public Kategorija(int id, String ime) {
        this.id = id;
        this.ime = ime;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIme() {
        return ime;
    }

    public void setIme(String ime) {
        this.ime = ime;
    }
    
}
